public class CharArithDemo {

	public static void main(String[] args) {
		char ch;
		
		ch = 'X';
		System.out.println("ch contains " + ch);
		
		ch++;		
		System.out.println("ch é agora " + ch);
		
		ch = 90;		
		System.out.println("ch é agora " + ch);		

	}

}
