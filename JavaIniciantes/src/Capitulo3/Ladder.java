package Capitulo3;

public class Ladder {

	public static void main(String[] args) {
				
		for (int i = 0; i < 6; i++) {
			if(i==1)
				System.out.println("i igual a 1");
			else if(i==2)
				System.out.println("i igual a 2");
			else if(i==3)
				System.out.println("i igual a 3");
			else if(i==4)
				System.out.println("i igual a 4");
			else 
				System.out.println("i não está entre 1 e 4");						
			
		}

	}

}
